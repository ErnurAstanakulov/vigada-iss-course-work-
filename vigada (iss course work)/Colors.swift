import UIKit

extension UIColor {
    struct VGDColor {
        static var pink: UIColor { return UIColor(red: 1.0, green: 0.376, blue: 0.765, alpha: 1) }
        static var orange: UIColor { return UIColor(red: 1.0, green: 0.333, blue: 0.259, alpha: 1) }
        static var green: UIColor { return UIColor(red: 0.137, green: 0.573, blue: 0.047, alpha: 1) }
        static var yellow: UIColor { return UIColor(red: 0.945, green: 0.749, blue: 0.082, alpha: 1) }
        static var blue: UIColor { return UIColor(red: 0.122, green: 0.62, blue: 1.0, alpha: 1) }
        static var lightGray: UIColor { return UIColor.lightGray }
        static var gray: UIColor { return UIColor.gray }
        static var darkGray: UIColor { return UIColor.darkGray }
        static var black: UIColor { return UIColor.black }
        static var white: UIColor { return UIColor.white }
        static var redmy: UIColor { return UIColor.red }
    }
}
